
package PersonalV2;

public class PersonalV2 {
    String nombre,apellido,categoria;
    String dni,cuit,fechaIngreso;
    double sueldo;

    public PersonalV2(String nombre, String apellido, String categoria, String dni, String cuit, String fechaIngreso, double sueldo) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.categoria = categoria;
        this.dni = dni;
        this.cuit = cuit;
        this.fechaIngreso = fechaIngreso;
        this.sueldo = sueldo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getCuit() {
        return cuit;
    }

    public void setCuit(String cuit) {
        this.cuit = cuit;
    }

    public String getFechaIngreso() {
        return fechaIngreso;
    }

    public void setFechaIngreso(String fechaIngreso) {
        this.fechaIngreso = fechaIngreso;
    }

    public double getSueldo() {
        return sueldo;
    }

    public void setSueldo(double sueldo) {
        this.sueldo = sueldo;
    }
    
}
